document.addEventListener('DOMContentLoaded', function() {
    const pages = [
        'pandemicblog.php',
        'business-imp-blog.php',
        'resumeblog.php',
        'mes.php'
    ];

    const previousButton = document.getElementById('previous').querySelector('a');
    const nextButton = document.getElementById('next').querySelector('a');
    const pageLinks = Array.from(document.querySelectorAll('.page-item a'));

    let currentPageIndex = 0;

    function updatePagination() {
        pageLinks.forEach((link, index) => {
            if (index === currentPageIndex + 1) {
                link.parentElement.classList.add('active');
            } else {
                link.parentElement.classList.remove('active');
            }
        });
    }

    function setLinks() {
        previousButton.href = pages[(currentPageIndex - 1 + pages.length) % pages.length];
        nextButton.href = pages[(currentPageIndex + 1) % pages.length];
        pageLinks.forEach((link, index) => {
            if (index > 0 && index < pageLinks.length - 1) {
                link.href = pages[index - 1];
            }
        });
    }

    previousButton.addEventListener('click', (e) => {
        e.preventDefault();
        currentPageIndex = (currentPageIndex - 1 + pages.length) % pages.length;
        setLinks();
        updatePagination();
        window.location.href = previousButton.href; // Navigate to the previous page
    });

    nextButton.addEventListener('click', (e) => {
        e.preventDefault();
        currentPageIndex = (currentPageIndex + 1) % pages.length;
        setLinks();
        updatePagination();
        window.location.href = nextButton.href; // Navigate to the next page
    });

    setLinks();
    updatePagination();
});
</script>



